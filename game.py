class Mario():
    def __init__(self, x, y):
        self.mario_img = pygame.image.load('mario.png')
        self.mario_img = pygame.transform.scale(self.mario_img, (70, 70))
        self.mario_img_left = pygame.transform.flip(self.mario_img, True, False)
        self.rect = self.mario_img.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.vel_y = 0
        self.direction = 0
        self.counter = 0
        self.index = 0

    def update(self):
        dx = 0
        dy = 0
        walk_cooldown = 5

        key = pygame.key.get_pressed()
        if key[pygame.K_SPACE]:
            self.vel_y = -15
        if key[pygame.K_LEFT]:
            dx -= 5
            self.direction = -1
        if key[pygame.K_RIGHT]:
            dx += 5
            self.direction = 1
        if key[pygame.K_LEFT] == False and key[pygame.K_RIGHT] == False:
            if self.direction == 1:
                self.mario_img = self.mario_img
            if self.direction == -1:
                self.mario_img = self.mario_img_left

        if self.counter > walk_cooldown:
                self.counter = 0
                self.index += 1
                if self.index >= len(self.images_right):
                    self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]

        self.rect.x += dx
        self.rect.y += dy

        screen.blit(self.mario_img, self.rect)


class World():
    def __init__(self, data):
        self.tile_list = []

        # load images
        ground_img = pygame.image.load('brick.png')
        block_img = pygame.image.load('block.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(
                        ground_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])
            pygame.draw.rect(screen, (255, 255, 255), tile[1], 2)


